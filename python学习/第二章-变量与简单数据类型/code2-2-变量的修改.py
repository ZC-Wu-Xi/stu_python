# 任务：变量的修改
year = 2023
print(year)  # 打印2023
year = 2024
print(year)  # 打印2024

# 不同类型的变量也可以进行修改
money = '十元'
money = 10  # 不同类型的变量也可以进行修改、重新赋值，与类型无关。
print(money)  # 打印10


# 任务：超市优惠活动
price = 10.5
